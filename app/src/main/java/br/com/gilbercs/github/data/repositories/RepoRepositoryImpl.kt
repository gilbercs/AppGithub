package br.com.gilbercs.github.data.repositories


import br.com.gilbercs.app.github.data.services.GitHubService
import br.com.gilbercs.github.core.RemoteException
import kotlinx.coroutines.flow.flow
import retrofit2.HttpException

class RepoRepositoryImpl(private val service: GitHubService) : RepoRepository {

    override suspend fun listRepositories(user: String) = flow {
        try {
            val repoList = service.listRepositories(user)
            emit(repoList)
        } catch (ex: HttpException) {
            throw RemoteException(ex.message ?: "NÃ£o foi possivel fazer a busca no momento!")
        }
    }
}